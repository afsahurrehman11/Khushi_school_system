# ==================== ACTIVITY TIMELINE (TASK 6) ====================

@router.get("/activity-timeline")
async def get_accounting_activity_timeline(
    page: int = Query(default=1, ge=1, description="Page number"),
    page_size: int = Query(default=20, ge=1, le=100, description="Items per page"),
    activity_types: Optional[str] = Query(None, description="Comma-separated activity types"),
    date_from: Optional[str] = Query(None, description="Start date YYYY-MM-DD"),
    date_to: Optional[str] = Query(None, description="End date YYYY-MM-DD"),
    accountant_id: Optional[str] = Query(None, description="Filter by accountant ID"),
    current_user: dict = Depends(get_current_user)
):
    """
    Get activity timeline showing all accounting events.
    TASK 6: Daily Activity Timeline
    
    Returns latest 100 activities by default with pagination.
    Admin/root see all, accountants see only their own.
    """
    role = current_user.get("role", "").lower()
    user_id = current_user.get("id") or current_user.get("user_id")
    
    # Security: Non-admins can only see their own data
    if role == "accountant":
        accountant_id = user_id
    elif role not in ["admin", "root"]:
        logger.warning(f"⚠️ Unauthorized statistics access attempt by {current_user.get('email')}")
        raise HTTPException(
            status_code=status.HTTP_403_FORBIDDEN,
            detail="Access denied."
        )
    
    logger.info(f"🕒 Accounting activity timeline generated by {current_user.get('email')}")
    
    try:
        school_id = current_user.get("school_id")
        
        if not school_id:
            raise HTTPException(status_code=400, detail="School context required")
        
        # Parse activity types
        types_list = None
        if activity_types:
            types_list = [t.strip() for t in activity_types.split(",")]
        
        result = get_activity_timeline(
            school_id=school_id,
            page=page,
            page_size=page_size,
            activity_types=types_list,
            date_from=date_from,
            date_to=date_to,
            accountant_id=accountant_id
        )
        
        return {"success": True, **result}
        
    except HTTPException:
        raise
    except Exception as e:
        logger.error(f"❌ Error getting activity timeline: {e}")
        raise HTTPException(status_code=500, detail=str(e))
