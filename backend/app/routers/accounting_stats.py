# ==================== ACCOUNTANT PERSONAL STATS (TASK 1) ====================

@router.get("/my-stats")
async def get_my_statistics(
    days_trend: int = Query(default=30, ge=7, le=90, description="Days for trend data"),
    current_user: dict = Depends(get_current_user)
):
    """
    Get personal statistics for the logged-in accountant.
    TASK 1: Accountant Personal Statistics Dashboard
    
    Only accountants can access their own statistics.
    """
    role = current_user.get("role", "").lower()
    
    # Security check - only accountants, admin, root can access
    if role not in ["accountant", "admin", "root"]:
        logger.warning(f"⚠️ Unauthorized statistics access attempt by {current_user.get('email')}")
        raise HTTPException(
            status_code=status.HTTP_403_FORBIDDEN,
            detail="Access denied. Accountant role required."
        )
    
    logger.info(f"📊 Loading accountant statistics dashboard for {current_user.get('email')}")
    
    try:
        user_id = current_user.get("id") or current_user.get("user_id")
        school_id = current_user.get("school_id")
        
        if not school_id:
            raise HTTPException(status_code=400, detail="School context required")
        
        stats = get_accountant_personal_stats(
            user_id=user_id,
            user_name=current_user.get("name", "Unknown"),
            user_email=current_user.get("email", "unknown"),
            school_id=school_id,
            days_trend=days_trend
        )
        
        return {"success": True, "data": stats}
        
    except HTTPException:
        raise
    except Exception as e:
        logger.error(f"❌ Error getting personal stats: {e}")
        raise HTTPException(status_code=500, detail=str(e))


